
package oop.Inheritance;


public class inheriting_private_member {
private String name;
private int age;

  public void setName(String n){
      name = n;
      
  }
  public String getName()
  {
      return name;
  }
  
  public void setAge(int age){
this.age =age;
}
public int getAge(){
return age;


}
}